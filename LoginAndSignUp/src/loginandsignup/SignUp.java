    private void SignUpBNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SignUpBNActionPerformed
        // TODO add your handling code here:
        String FullName = FullNameTF.getText();
        String Email = EmailTF.getText();
        String PhoneNumber = PhoneNumberTF.getText();
        String Password = PasswordTF.getText();
        
        if(FullName.length()<3){
            JOptionPane.showMessageDialog(this, "Name must be at least 3 characters long.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
         String EmailTF = "^[\\w-\\.]+@([\\w-]+\\.)+[\\w-]{2,4}$";
        if(!Email.matches(EmailTF)){
            JOptionPane.showMessageDialog(this, "Invalid email address.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (PhoneNumber.length() != 10 || !PhoneNumber.matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "Phone number must be 10 digits long.", "Error", JOptionPane.ERROR_MESSAGE);
            return;}
        
         String PasswordTF = "^(?=.*[a-z])(?=.*[A-Z])(?=.*\\d).{8,}$";
        if (!PasswordTF.matches(PasswordTF)) {
            JOptionPane.showMessageDialog(this, "Password must contain uppercase, lowercase, and a digit.", "Error", JOptionPane.ERROR_MESSAGE);
             
        }
        
        else{
        JOptionPane.showMessageDialog(this, "Login successful!", "Success", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_SignUpBNActionPerformed

